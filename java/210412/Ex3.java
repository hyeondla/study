
public class Ex3 {

	public static void main(String[] args) {

		//do~while문
		
		int i = 11;
		
		while(i <= 10) {
			System.out.println(i);
			i++;
		}
		System.out.println("while문 종료 후 i 값 = " + i);
		
		System.out.println("==========================");
		
		i = 11;
		
		do {
			System.out.println(i);
			i++;
		} while(i <= 10);
		System.out.println("do~while문 종료 후 i 값 = " + i);
		
		
		
	}

}
